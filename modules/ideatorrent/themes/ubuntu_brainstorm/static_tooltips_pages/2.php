<h1>Ubuntu blueprints</h1>

<p>A blueprint is a document that <b>you</b> can write and that describe a feature, and how to implement it. We can compare a blueprint to an idea, but with concrete technical directions on how to make it work.</p>
<p>Mature blueprints are chosen and then discussed at the Ubuntu Developer Conferences just after a Ubuntu release, to decide what to do for the next release. For example, here is <a href="https://blueprints.launchpad.net/ubuntu/gutsy" target="_blank">the list of the blueprints</a> that have been targeted for the gutsy 7.10 release. 
</p>
<ul>
<li>To search in the existing blueprints, <a href="https://blueprints.launchpad.net/ubuntu/" target="_blank">please follow this link</a>.</li>
<li>To create a new blueprint, <a href="https://blueprints.launchpad.net/ubuntu/+addspec" target="_blank">please follow this link</a>.</li>
</ul>

